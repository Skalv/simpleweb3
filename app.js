class MyDapp {
  constructor(ABI) {
    this.web3 = new Web3(Web3.givenProvider)
    this.contract = new this.web3.eth.Contract(ABI, "0x7FFd018c0CfAE692bC6d5818e5FD443D246D14C2")
    this.cart = []

    document.getElementById('LoginBtn').addEventListener('click', () => {
      this.connect()
    })
    document.getElementById('AddWhitelist').addEventListener('submit', (event) => {
      event.preventDefault()
      const accountToAdd = document.getElementsByClassName('adrToWhitelist')[0].value
      this.addToWhitelist(accountToAdd)
    })
    document.getElementById('MintForm').addEventListener('submit', (event) => {
      event.preventDefault()
      const tokenToAdd = document.getElementsByClassName('tokenToAdd')[0].value
      this.addCart(tokenToAdd)
    })

    document.getElementById('Mint').addEventListener('click', () => {
      this.mint()
    })

    this.getTotalSupply()
  }

  connect() {
    ethereum
      .request({ method: 'eth_requestAccounts' })
      .then(accounts => {
        this.account = accounts[0]
        console.log("connected !", this.account)

        document.getElementById('LoginBtn').style.display = 'none'
        document.getElementsByClassName('myAccount')[0].append(this.account)
        document.getElementsByClassName('accountContainer')[0].style.display = 'block'
        this.checkIsWhitelist()
        this.checkWallet()
      }).catch(err => {
        console.error('ERROR : ', err.message)
      })
  }

  getTotalSupply() {
    this.contract.methods
      .totalSupply()
      .call()
      .then(result => {
        document.getElementsByClassName('totalMint')[0].append(result)
      }).catch(err => {
        console.error(err)
      })
  }

  checkIsWhitelist() {
    this.contract.methods
      .isWhitelisted(this.account)
      .call()
      .then(response => {
        if (response) {
          document.getElementsByClassName('accountContainer')[0].style.color = 'green'
        } else {
          document.getElementsByClassName('accountContainer')[0].style.color = 'red'
        }
      }).catch(err => {
        console.error(err)
      })
  }

  checkWallet() {
    this.contract.methods
      .walletOfOwner(this.account)
      .call()
      .then(response => {
        if (response) {
          console.log(response)
        } else {
          console.log("wallet empty")
        }
      }).catch(err => {
        console.error(err)
      })
  }

  addToWhitelist(accountToAdd) {
    // Attention c'est long d'avoir une réponse (temps du minage)
    // Donc indiquer que ca charge pour pas que l'utilisateur click partout
    this.contract.methods
      .addToWhitelist(accountToAdd)
      .send({
        from: this.account // only owner of contract
      }).then(response => {
        console.log(response)
      }).catch(err => {
        console.log(err)
      })
  }

  mint() {
    if (this.cart.length <= 0) return

    let tokenIds = []
    let hashSigns = []

    for (let i = 0; i < this.cart.length; i++) {
      tokenIds.push(this.cart[i].tokenId)
      hashSigns.push(this.cart[i].signHash)
    }

    const price = this.web3.utils.toWei(`${this.getTotalPrice()}`)

    console.log(tokenIds, hashSigns, price)
    // Attention c'est long d'avoir une réponse (temps du minage)
    // Donc indiquer que ca charge pour pas que l'utilisateur click partout
    this.contract.methods
      .mintNFT(tokenIds, hashSigns)
      .send({
        from: this.account,
        value: this.web3.utils.toHex(price)
      }).then(response => {
        console.log(response)
      }).catch(err => {
        console.log(err)
      })
  }

  addCart(tokenId) {
    this.contract.methods
      .getCartHash(tokenId)
      .call({
        from: this.account
      })
      .then(cartHash => {
        return ethereum.request({
          method: "personal_sign",
          params: [
            this.account,
            cartHash
          ]
        })
        // var msgHash = ethUtil.keccak256('An amazing message, for use with MetaMask!')
      }).then(signHash => {
        this.cart.push({
          tokenId,
          signHash
        })

        const listEl = document.getElementsByClassName('CartContent')[0]
        listEl.innerHTML = ""
        for (let i = 0; i < this.cart.length; i++) {
          const li = document.createElement("li")
          li.append(`NFT #${this.cart[i].tokenId} -> ${this.cart[i].signHash}`)
          listEl.append(li)
        }
      })
      .catch(err => {
        console.error(err)
      })
  }

  getTotalPrice() {
    let total = 0
    for (let i = 0; i < this.cart.length; i++) {
      if (this.cart[i].tokenId <= 2) {
        total += 10
      } else if (this.cart[i].tokenId > 2 && this.cart[i].tokenId <= 16) {
        total += 1
      } else if (this.cart[i].tokenId > 16 && this.cart[i].tokenId <= 119) {
        total += 0.1
      } else if (this.cart[i].tokenId > 119 && this.cart[i].tokenId <= 915) {
        total += 0.01
      }
    }

    return total
  }
}

